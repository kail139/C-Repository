#include <stdio.h>

int main () {

  int a;
  printf("\n***This Program Prints The Given Integer Value In Octal And Hexadecimal!***");
  printf("\n\n\nEnter A Integer Number:");
  scanf("%d",&a);
  printf("\n\nThe Octal Value Of %d Is %o !",a,a);
  printf("\n\nThe Hexadecimal Value Of %d Is %x",a,a);

return 0;
}
